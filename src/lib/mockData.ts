function generateListingAnalytics(): ListingAnalyticsSummary {
  const daily = [];
  const now = new Date('2026-04-15');
  for (let i = 29; i >= 0; i--) {
    const d = new Date(now);
    d.setDate(d.getDate() - i);
    const dayFactor = 1 + Math.sin(i * 0.3) * 0.4;
    const trendFactor = 1 + (30 - i) * 0.02;
    daily.push({
      date: d.toISOString().split('T')[0],
      views: Math.round((35 + Math.random() * 50) * dayFactor * trendFactor),
      clicks: Math.round((8 + Math.random() * 15) * dayFactor * trendFactor),
      applications: Math.round((1 + Math.random() * 4) * dayFactor),
    });
  }
  const total_views = daily.reduce((s, d) => s + d.views, 0);
  const total_clicks = daily.reduce((s, d) => s + d.clicks, 0);
  const total_applications = daily.reduce((s, d) => s + d.applications, 0);
  return {
    total_views,
    total_clicks,
    total_applications,
    views_delta: 18.3,
    clicks_delta: 12.7,
    applications_delta: -4.2,
    daily,
  };
}

